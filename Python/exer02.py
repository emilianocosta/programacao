#Calcula a area de um quadrado
#--------|---------|---------|---------|---------|---------|---------|---------|---------|---------|---------|---------|

print("Calcular e exibir a área de um quadrado, a partir do valor de sua aresta(lado) que será digitado")

#entrada dados
aresta = float(input("Digite a aresta : "))

#calcula
area = aresta * aresta

#mostra resultado
print(f"O quadrado tem area = {area: .2f} ")
