#Calcula a area de um retangulo
#--------|---------|---------|---------|---------|---------|---------|---------|---------|---------|---------|---------|

print("Entrar via teclado com a base e a altura de um retângulo, calcular e exibir sua área")

#entrada dados
base = float(input("Digite a base : "))
